package TestJava05;

public class Ex04 {

	public static void main(String[] args) {
		
		// 식을 만든다(공통부분)
		// 초기값 조간식 증감식
		
		
			
			int sum = 0;
			
			
			for(int i=1; i<=10 ; i++ ) {
				sum = sum+i;
				System.out.println(i + "까지의 합은" + sum + "입니다.");
			}
			
			System.out.println("1부터 10까지의 합은" + sum + "입니다.");
		
		
		

	}

}
